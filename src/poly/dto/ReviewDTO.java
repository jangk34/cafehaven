package poly.dto;

public class ReviewDTO {

	
	private String reviewNo;
	private String cafeReview;
	private String cafeStar;
	private String menuName;
	private String rvWriter;
	private String regNo;
	private String regDate;
	private String updDate;
	private String updNo;
	
	public String getReviewNo() {
		return reviewNo;
	}
	public void setReviewNo(String reviewNo) {
		this.reviewNo = reviewNo;
	}
	public String getCafeReview() {
		return cafeReview;
	}
	public void setCafeReview(String cafeReview) {
		this.cafeReview = cafeReview;
	}
	public String getCafeStar() {
		return cafeStar;
	}
	public void setCafeStar(String cafeStar) {
		this.cafeStar = cafeStar;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getRvWriter() {
		return rvWriter;
	}
	public void setRvWriter(String rvWriter) {
		this.rvWriter = rvWriter;
	}
	public String getRegNo() {
		return regNo;
	}
	public void setRegNo(String regNo) {
		this.regNo = regNo;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getUpdDate() {
		return updDate;
	}
	public void setUpdDate(String updDate) {
		this.updDate = updDate;
	}
	public String getUpdNo() {
		return updNo;
	}
	public void setUpdNo(String updNo) {
		this.updNo = updNo;
	}
}
