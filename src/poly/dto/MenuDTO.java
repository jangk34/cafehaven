package poly.dto;

public class MenuDTO {

	private String menuNo;
	private String menuName;
	private String menuIntro;
	private String menuPrice;
	private String menuAmount;
	private String menuCgNo;
	private String regDate;
	private String regNo;
	private String updDate;
	private String updNo;
	
	public String getMenuNo() {
		return menuNo;
	}
	public void setMenuNo(String menuNo) {
		this.menuNo = menuNo;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public String getMenuIntro() {
		return menuIntro;
	}
	public void setMenuIntro(String menuIntro) {
		this.menuIntro = menuIntro;
	}
	public String getMenuPrice() {
		return menuPrice;
	}
	public void setMenuPrice(String menuPrice) {
		this.menuPrice = menuPrice;
	}
	public String getMenuAmount() {
		return menuAmount;
	}
	public void setMenuAmount(String menuAmount) {
		this.menuAmount = menuAmount;
	}
	public String getMenuCgNo() {
		return menuCgNo;
	}
	public void setMenuCgNo(String menuCgNo) {
		this.menuCgNo = menuCgNo;
	}
	public String getRegDate() {
		return regDate;
	}
	public void setRegDate(String regDate) {
		this.regDate = regDate;
	}
	public String getRegNo() {
		return regNo;
	}
	public void setRegNo(String regNo) {
		this.regNo = regNo;
	}
	public String getUpdDate() {
		return updDate;
	}
	public void setUpdDate(String updDate) {
		this.updDate = updDate;
	}
	public String getUpdNo() {
		return updNo;
	}
	public void setUpdNo(String updNo) {
		this.updNo = updNo;
	}
	
}
